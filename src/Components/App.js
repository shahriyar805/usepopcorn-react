function MoviesList({ movies }) {
  return (
    <ul className="list">
      {movies?.map((movie) => (
        <Movie movie={movie} key={movie.imdbID} />
      ))}
    </ul>
  );
}

function Movie({ movie }) {
  return (
    <li>
      <img src={movie.Poster} alt={`${movie.Title} poster`} />
      <h3>{movie.Title}</h3>
      <div>
        <MovieDetail emoji="üóì">{movie.Year}</MovieDetail>
      </div>
    </li>
  );
}

function WatchedSummary({ watched }) {
  const avgImdbRating = average(watched.map((movie) => movie.imdbRating));
  const avgUserRating = average(watched.map((movie) => movie.userRating));
  const avgRuntime = average(watched.map((movie) => movie.runtime));

  return (
    <div className="summary">
      <h2>Movies you watched</h2>
      <div>
        <MovieDetail emoji="#Ô∏è‚É£">{watched.length} movies</MovieDetail>
        <MovieDetail emoji="‚≠êÔ∏è">{avgImdbRating}</MovieDetail>
        <MovieDetail emoji="üåü">{avgUserRating}</MovieDetail>
        <MovieDetail emoji="‚è≥">{avgRuntime} min</MovieDetail>
      </div>
    </div>
  );
}

